package Bitwise_Operations;

public class LeftShift_Operator {

	public static void main(String[] args) {
		System.out.println(10<<1); // convert into 20
		System.out.println(20<<1); // convert into 40
		System.out.println(30<<1); // convert into 60
		System.out.println("------------------------");
		System.out.println(10<<2); // convert into 20
		System.out.println(20<<2); // convert into 40
		System.out.println(30<<2); // convert into 60
	}

}
